$( function ra() {
    $( "#dialog1" ).dialog({
      autoOpen: false,
      show: {
        effect: "blind",
        duration: 1000
      },
      hide: {
        effect: "explode",
        duration: 1000
      }
    });
 
    $( "#ra" ).on( "click", function() {
      $( "#dialog1" ).dialog( "open" );
    });
  } );
  
